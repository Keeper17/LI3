package GestaoVendas.Auxiliary_Classes;

public class StringDouble {
    private String str;
    private double db;

    public StringDouble(String str, double db){
        this.str = str;
        this.db = db;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }

    public double getDb() {
        return db;
    }

    public void setDb(double db) {
        this.db = db;
    }
}
